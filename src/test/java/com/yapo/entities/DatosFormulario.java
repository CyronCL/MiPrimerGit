package com.yapo.entities;

public class DatosFormulario {
	String categoria;
	String tipo_de_inmueble;
	String dormitorios;
	String Banos;

	public String getCategoria() {
		return categoria;
	}

	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}

	public String getTipo_de_inmueble() {
		return tipo_de_inmueble;
	}

	public void setTipo_de_inmueble(String tipo_de_inmueble) {
		this.tipo_de_inmueble = tipo_de_inmueble;
	}

	public String getDormitorios() {
		return dormitorios;
	}

	public void setDormitorios(String dormitorios) {
		this.dormitorios = dormitorios;
	}

	public String getBanos() {
		return Banos;
	}

	public void setBanos(String banos) {
		Banos = banos;
	}
}
